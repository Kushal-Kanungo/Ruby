# ? Data Types
my_int = 1
my_float = 1.23
my_boolean = true
my_nil = nil
my_string = 'Kushal'

puts my_int
puts my_float
puts my_boolean
puts my_nil
puts my_string

# Normal Concatenation
puts 'My name is ' + my_string

# String Interpolation
puts "My name is #{my_string}"

# ? Casting and Coverting
puts 'Casting and Coverting'
puts 3.14.to_i # To Int
puts 2.to_f   # TO float
puts '3.2'.to_i # to int
puts 2.2.to_s # to string

